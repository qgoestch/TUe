function [A] = Area_vec(P1,P2,P3)
 A = zeros(3,1);
 A(1) = (P3(2)-P1(2))*(P2(3)-P1(3)) - (P2(2)-P1(2))*(P3(3)-P1(3));
 A(2) = (P3(3)-P1(3))*(P2(1)-P1(1)) - (P2(3)-P1(3))*(P3(1)-P1(1));
 A(3) = (P3(1)-P1(1))*(P2(2)-P1(2)) - (P2(1)-P1(1))*(P3(2)-P1(2));
 S = norm(cross((P3-P1),(P2-P1)))/2;
 A = (S*A)./norm(A);
end